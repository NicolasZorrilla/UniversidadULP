    private void limpiarFilas() {
        for (int i = modelo.getRowCount() -1; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }

    private void cargarComboAlumno() {
        AlumnoData alumnodata = new AlumnoData();
        for (Alumno alumno : alumnodata.listarAlumnos()) {
            comboAlumno.addItem(alumno);
        }
    }
